package entity;

public class Product {
	private int id;
	private String name;
	private String img;
	private int power;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getImg() {
		return img;
	}
	public void setImg(String img) {
		this.img = img;
	}
	public int getPower() {
		return power;
	}
	public void setPower(int power) {
		this.power = power;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	@Override
	public String toString() {
		return "Product [id=" + id + ", name=" + name + ", img=" + img + ", power=" + power + "]";
	}
	public Product(int id, String name, String img, int power) {
		super();
		this.id = id;
		this.name = name;
		this.img = img;
		this.power = power;
	}
	public Product() {}
}
